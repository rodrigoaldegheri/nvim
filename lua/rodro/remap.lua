vim.keymap.set("n", "<leader>k", function()
  local filename = vim.fn.expand("%:t")
  local name_without_extension = filename:gsub("%.md$", "")
  vim.cmd('silent exec "!pandoc ' .. filename .. ' -o ' .. name_without_extension .. '.pdf &"')
end, { noremap = true, silent = true })

vim.keymap.set("n", "<leader>z", function()
  local filename = vim.fn.expand('%:t')                                    -- Get the current filename (e.g., document.md)
  local name_without_extension = filename:gsub('%.md$', '')                -- Remove the .md extension
  vim.cmd('silent exec "!zathura ' .. name_without_extension .. '.pdf &"') -- Open the compiled PDF in Zathura
end, { noremap = true, silent = true })



